define :prophet_bass do |s, q, b1, b2, cs, c0, c1, w|
  use_random_seed s
  32.times do    
    nt = (rand < b1) ? :e1 : :e2
    amp = (rand < b2) ? 0.75 : 1.5
    sn = synth :prophet, note: nt, amp: amp, cutoff: rrand(c0, c0+w), cutoff_slide: cs if rand < q
    control sn, cutoff: rrand(c1, c1+w)
    sleep 0.125
  end
end

